using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI; //パネルのイメージを操作するのに必要
using UnityEngine.SceneManagement;

public class Fade : MonoBehaviour
{
    float fadeSpeed = 0.01f;        //透明度が変わるスピードを管理
    float red, green, blue, alfa;   //パネルの色、不透明度を管理

    public bool isFadeOut = false;  //フェードアウト処理の開始、完了を管理するフラグ
    public bool isFadeIn = false;   //フェードイン処理の開始、完了を管理するフラグ
    public string changeSceneName; // フェードアウト処理後、シーン遷移する場合のシーン名

    Image fadeImage;                //透明度を変更するパネルのイメージ

    public bool isfade;

    void Start()
    {
        fadeImage = GetComponent<Image>();
        red = fadeImage.color.r;
        green = fadeImage.color.g;
        blue = fadeImage.color.b;
        alfa = fadeImage.color.a;
        isfade = false;
    }

    void Update()
    {

        if (isFadeIn)
        {
            StartFadeIn();
        }

        if (isFadeOut)
        {
            StartFadeOut();
        }
    }

    void StartFadeIn()
    {
        alfa -= fadeSpeed;                //a)不透明度を徐々に下げる
        SetAlpha();                      //b)変更した不透明度パネルに反映する
        if (alfa <= 0)
        {                    //c)完全に透明になったら処理を抜ける
            isFadeIn = false;
            isfade = false;
        }
    }

    void StartFadeOut()
    {
        isfade = true;
        fadeImage.enabled = true;  // a)パネルの表示をオンにする
        alfa += fadeSpeed;         // b)不透明度を徐々にあげる
        SetAlpha();               // c)変更した透明度をパネルに反映する
        if (alfa >= 1.5)
        {             // d)完全に不透明になったら処理を抜ける
            isFadeOut = false;
            isFadeIn = true;
            if (changeSceneName != "")
            {
                Debug.Log(changeSceneName + "に遷移します。");
                SceneManager.LoadScene(changeSceneName);
            }

        }
    }

    void SetAlpha()
    {
        fadeImage.color = new Color(red, green, blue, alfa);
    }

    //　スタートボタンを押したら実行する
    public void GameStart()
    {
        if (isfade) return;
        isFadeOut = true;
        changeSceneName = "Master_Final2";
    }

    public void GameSelect()
    {
        if (isfade) return;
        isFadeOut = true;
        changeSceneName = "nagaya";
    }

    public void GameTitle()
    {
        if (isfade) return;
        isFadeOut = true;
        changeSceneName = "Title";
    }

    public void GameOver()
    {
        if (isfade) return;
        isFadeOut = true;
        changeSceneName = "GameOver";
    }

    public bool Fadeflag()
    {
        return isfade;
    }
}
